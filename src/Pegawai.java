public class Pegawai {
    private String namaPegawai;
    private String noPegawai;
    private String alamatPegawai;
    private String uidPegawai;
    private String passPegawai;

    public String getNamaPegawai() {
        return namaPegawai;
    }

    public void setNamaPegawai(String namaPegawai) {
        this.namaPegawai = namaPegawai;
    }

    public String getNoPegawai() {
        return noPegawai;
    }

    public void setNoPegawai(String noPegawai) {
        this.noPegawai = noPegawai;
    }

    public String getAlamatPegawai() {
        return alamatPegawai;
    }

    public void setAlamatPegawai(String alamatPegawai) {
        this.alamatPegawai = alamatPegawai;
    }

    public String getUidPegawai() {
        return uidPegawai;
    }

    public void setUidPegawai(String uidPegawai) {
        this.uidPegawai = uidPegawai;
    }

    public String getPassPegawai() {
        return passPegawai;
    }

    public void setPassPegawai(String passPegawai) {
        this.passPegawai = passPegawai;
    }
}
