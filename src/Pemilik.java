public class Pemilik {
    private String nikPemilik;
    private String namaPemilik;
    private String uidPemilik;
    private String passPemilik;

    public String getNikPemilik() {
        return nikPemilik;
    }

    public void setNikPemilik(String nikPemilik) {
        this.nikPemilik = nikPemilik;
    }

    public String getNamaPemilik() {
        return namaPemilik;
    }

    public void setNamaPemilik(String namaPemilik) {
        this.namaPemilik = namaPemilik;
    }

    public String getUidPemilik() {
        return uidPemilik;
    }

    public void setUidPemilik(String uidPemilik) {
        this.uidPemilik = uidPemilik;
    }

    public String getPassPemilik() {
        return passPemilik;
    }

    public void setPassPemilik(String passPemilik) {
        this.passPemilik = passPemilik;
    }
}
